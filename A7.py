x1 = int(input("Введите первую координату X первой точки "))
y1 = int(input("Введите вторую координату Y первой точки "))
x2 = int(input("Введите первую координату X второй точки "))
y2 = int(input("Введите вторую координату Y второй точки "))

same_color = (x1 + y1) % 2 == (x2 + y2) % 2

if same_color:
    print(f"Клетки ({x1},{y1}) и ({x2},{y2}) -> YES, {'White' if (x1 + y1) % 2 == 0 else 'Black'}")
else:
    print(f"Клетки ({x1},{y1}) и ({x2},{y2}) -> NO")